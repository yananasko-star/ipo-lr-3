import math  # библиотека для корня и работы с числами

# Ввод данных от пользователя
x = float(input("Введите координату x: "))
y = float(input("Введите координату y: "))
R = float(input("Введите радиус круга R: "))

# Формула расстояния от точки (x, y) до начала координат (0, 0):
# distance = √(x² + y²)
distance = math.sqrt(x**2 + y**2)

# Проверяем, попадает ли точка внутрь круга или на его границу
if distance <= R:
    print("Точка находится внутри круга или на его границе.")
else:
    print("Точка находится вне круга.")