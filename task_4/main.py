import math  # импортируем библиотеку для работы с корнями и другими функциями

# Запрашиваем коэффициенты у пользователя
a = float(input("Введите коэффициент a: "))
b = float(input("Введите коэффициент b: "))
c = float(input("Введите коэффициент c: "))

# Вычисляем дискриминант: D = b^2 - 4ac
D = b**2 - 4*a*c

print(f"Дискриминант: {D}")

# Проверяем количество решений
if D > 0:  # если дискриминант положительный → 2 корня
    x1 = (-b + math.sqrt(D)) / (2*a)
    x2 = (-b - math.sqrt(D)) / (2*a)
    print(f"Уравнение имеет два действительных корня: x1 = {x1}, x2 = {x2}")
elif D == 0:  # если дискриминант равен 0 → 1 корень
    x = -b / (2*a)
    print(f"Уравнение имеет один действительный корень: x = {x}")
else:  # если дискриминант отрицательный → действительных корней нет
    print("Уравнение не имеет действительных корней.")


